/* ─── Markdown-like renderer ────────────────────────────────────────────────── */
const renderContent = (content: string) => {
  return content.split("\n").map((line, i) => {
    // Bold section headers
    if (line.startsWith("**") && line.endsWith("**")) {
      return (
        <h3 key={i} className="text-lg font-bold text-foreground mt-8 mb-3 first:mt-0">
          {line.slice(2, -2)}
        </h3>
      );
    }
    // Bullet points
    if (line.startsWith("• ")) {
      return (
        <li key={i} className="text-sm text-muted-foreground leading-relaxed ms-5 mb-1.5 list-disc">
          {line.slice(2)}
        </li>
      );
    }
    // Numbered items
    const numMatch = line.match(/^(\d+)\.\s(.+)/);
    if (numMatch) {
      return (
        <li key={i} className="text-sm text-muted-foreground leading-relaxed ms-5 mb-1.5 list-decimal">
          {numMatch[2]}
        </li>
      );
    }
    // Empty line
    if (line.trim() === "") return <div key={i} className="h-2" />;
    // Normal paragraph
    return (
      <p key={i} className="text-sm text-muted-foreground leading-[1.85] mb-1">
        {line}
      </p>
    );
  });
};

